// Cache for loaded icons to avoid re-reading files
const iconCache = new Map();

/**
 * Available icon variants per size
 */
export const ICON_VARIANTS = {
  16: ['solid'],
  20: ['solid'],
  24: ['outline', 'solid'],
};

/**
 * Default configuration
 */
export const DEFAULT_CONFIG = {
  size: 24,
  variant: 'outline',
  class: '',
};

/**
 * Get HeroIcon SVG markup
 *
 * @param {string} name - Icon name (e.g., 'home', 'user', 'cog-6-tooth')
 * @param {Object} options - Configuration options
 * @param {number} options.size - Icon size: 16, 20, or 24 (default: 24)
 * @param {string} options.variant - Icon variant: 'outline' or 'solid' (default: 'outline')
 * @param {string} options.class - Additional CSS classes to add to the SVG
 * @param {string} options.width - Custom width (default: based on size)
 * @param {string} options.height - Custom height (default: based on size)
 * @returns {string} SVG markup string
 *
 * @example
 * heroicon('home')
 * heroicon('home', { variant: 'solid' })
 * heroicon('home', { size: 20, variant: 'solid' })
 * heroicon('home', { class: 'w-8 h-8 text-primary' })
 */
export function heroicon(name, options = {}) {
  const config = { ...DEFAULT_CONFIG, ...options };
  const { size, variant, class: className, width, height } = config;

  // Validate size
  if (![16, 20, 24].includes(size)) {
    console.warn(`Invalid size "${size}". Using default size 24.`);
    config.size = 24;
  }

  // Validate variant for size
  const availableVariants = ICON_VARIANTS[config.size];
  if (!availableVariants.includes(variant)) {
    console.warn(
      `Variant "${variant}" not available for size ${config.size}. Using "${availableVariants[0]}".`
    );
    config.variant = availableVariants[0];
  }

  const cacheKey = `${config.size}/${config.variant}/${name}`;

  // Return cached SVG if available (modified for options)
  if (iconCache.has(cacheKey)) {
    return modifySvg(iconCache.get(cacheKey), { className, width, height });
  }

  // For browser environment, we need to use inline SVGs
  // This provides a selection of commonly used icons
  const inlineIcons = getInlineIcons();
  const iconKey = `${config.size}-${config.variant}-${name}`;

  if (inlineIcons[iconKey]) {
    iconCache.set(cacheKey, inlineIcons[iconKey]);
    return modifySvg(inlineIcons[iconKey], { className, width, height });
  }

  // Fallback: return placeholder if icon not found
  console.warn(`Icon "${name}" not found in size ${config.size}/${config.variant}`);
  return `<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 ${config.size} ${config.size}" fill="none" stroke="currentColor" class="${className}"><rect x="2" y="2" width="${config.size - 4}" height="${config.size - 4}" stroke-dasharray="2"/></svg>`;
}

/**
 * Modify SVG markup with custom options
 */
function modifySvg(svg, { className, width, height }) {
  let result = svg;

  // Add class if specified
  if (className) {
    result = result.replace('<svg ', `<svg class="${className}" `);
  }

  // Add custom width/height if specified
  if (width) {
    result = result.replace(/width="[^"]*"/, `width="${width}"`);
    if (!result.includes('width=')) {
      result = result.replace('<svg ', `<svg width="${width}" `);
    }
  }
  if (height) {
    result = result.replace(/height="[^"]*"/, `height="${height}"`);
    if (!result.includes('height=')) {
      result = result.replace('<svg ', `<svg height="${height}" `);
    }
  }

  return result;
}

